package org.sola.admin.services.ejbs.admin.businesslogic.repository.entities;

import jakarta.persistence.Column;
import jakarta.persistence.Id;
import jakarta.persistence.Table;
import org.sola.services.common.repository.entities.AbstractVersionedEntity;

@Table(schema="system", name="project_appuser")
public class UserProject extends AbstractVersionedEntity {
    @Id
    @Column(name = "project_id")
    private String projectId;
    
    @Id
    @Column(name = "appuser_id")
    private String userId;
    
    public UserProject(){
        super();
    }

    public UserProject(String projectId, String userId){
        super();
        this.projectId = projectId;
        this.userId = userId;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getProjectId() {
        return projectId;
    }

    public void setProjectId(String projectId) {
        this.projectId = projectId;
    }
}
